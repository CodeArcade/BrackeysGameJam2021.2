        public Application(Vector2 spawnPoint, Microsoft.Xna.Framework.Rectangle tableArea) : base(spawnPoint, tableArea)
        {
            Texture = ContentManager.ApplicationSmallTexture;
            Size = SizeManager.GetSize(150, 200);

            Sex = GetSex();
            Country = GetCountry();
            Department = GetDepartment();
            Name = GetName(Sex);

            Load(spawnPoint, tableArea);
        }

        private Sexes GetSex()
        {
            return Random.Next(0, 3) switch
            {
                0 => Sexes.Female,
                1 => Sexes.Male,
                _ => Sexes.Other
            };
        }

        private Countries GetCountry()
        {
            return Random.Next(0, 11) switch
            {
                0 => Countries.America,
                1 => Countries.Canada,
                2 => Countries.Denmark,
                3 => Countries.Finland,
                4 => Countries.Germany,
                5 => Countries.Mexico,
                6 => Countries.Netherlands,
                7 => Countries.Norway,
                8 => Countries.Russia,
                9 => Countries.Sweden,
                _ => Countries.UnitedKingdom,
            };
        }

        public override List<Component> GetDetailViewComponents()
        {
            Size size = SizeManager.GetSize(450, 600);

            Sprite sprite = new Sprite()
            {
                Size = size,
                Position = new Vector2(
                        (int)(SizeManager.ScaleForWidth(SizeManager.JamGame.BaseWidth) - size.Width) / 2,
                        (int)(SizeManager.ScaleForHeight(SizeManager.JamGame.BaseHeight) - size.Height) / 2
                    ),
                Texture = ContentManager.ApplicationTexture
            };

            FontSystem fontSystem = new FontSystem();
            fontSystem.AddFont(SizeManager.JamGame.Content.OpenStream("Fonts/Arial.ttf"));

            Grid grid = new Grid();
            grid.ColumnsProportions.Add(new Proportion(ProportionType.Pixels, sprite.Size.Width));
            grid.RowsProportions.Add(new Proportion(ProportionType.Pixels, sprite.Size.Height * 0.3f));
            grid.RowsProportions.Add(new Proportion(ProportionType.Pixels, sprite.Size.Height * 0.2f));
            grid.RowsProportions.Add(new Proportion(ProportionType.Pixels, sprite.Size.Height * 0.2f));

            Label subjectLabel = new Label()
            {
                Text = $"Application for {Department}",
                GridColumn = 0,
                GridRow = 1,
                HorizontalAlignment = HorizontalAlignment.Stretch,
                VerticalAlignment = VerticalAlignment.Stretch,
                Font = fontSystem.GetFont((int)SizeManager.ScaleForWidth(24)),
                TextColor = Microsoft.Xna.Framework.Color.Black,
                Padding = new Myra.Graphics2D.Thickness((int)SizeManager.ScaleForWidth(100), 0, 0, 0)
            };
            grid.Widgets.Add(subjectLabel);

            Label infoLabel = new Label()
            {
                Text = $"{Name}{Environment.NewLine}{Sex}{Environment.NewLine}{Country}",
                GridColumn = 0,
                GridRow = 2,
                HorizontalAlignment = HorizontalAlignment.Stretch,
                VerticalAlignment = VerticalAlignment.Stretch,
                Font = fontSystem.GetFont((int)SizeManager.ScaleForWidth(22)),
                TextColor = Microsoft.Xna.Framework.Color.Black,
                Padding = new Myra.Graphics2D.Thickness((int)SizeManager.ScaleForWidth(25), 0, 0, 0)
            };
            grid.Widgets.Add(infoLabel);

            return new List<Component>() { sprite, new UiGridComponent(grid, sprite.Size, sprite.Position) };
        }

        private Departments GetDepartment()
        {
            return Random.Next(0, 10) switch
            {
                0 => Departments.Accounting,
                1 => Departments.Design,
                2 => Departments.Export,
                3 => Departments.HR,
                4 => Departments.IT,
                5 => Departments.Logistics,
                6 => Departments.Marketing,
                7 => Departments.PR,
                8 => Departments.Purchasing,
                _ => Departments.Research
            };
        }

        private string GetName(Sexes sex)
        {
            string name = "";

            if (sex == Sexes.Male || sex == Sexes.Other) name = MaleNames[Random.Next(0, MaleNames.Count)];
            if (sex == Sexes.Female) name = FemaleNames[Random.Next(0, FemaleNames.Count)];

            return $"{name} {LastNames[Random.Next(0, LastNames.Count)]}";
        }

